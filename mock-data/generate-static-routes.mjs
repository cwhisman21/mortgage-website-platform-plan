import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { DEFAULT_SITE_ORIGIN } from "../site/document-metadata.mjs";
import {
  createStaticRouteContext,
  renderStaticRouteDocument,
  renderStaticRouteRedirectDocument,
} from "../site/static-route-document.mjs";

const mockDataDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(mockDataDir, "..");

export const GENERATED_ROUTES_DIR = path.join(repoRoot, "site", "generated", "routes");

const readJson = (relativePath) => JSON.parse(fs.readFileSync(path.join(repoRoot, relativePath), "utf8"));

function loadLocationNewsIndex() {
  const index = readJson("mock-data/location-news-index.json");
  const articles = Array.isArray(index.articles) ? index.articles : [];
  const newsRoot = path.resolve(repoRoot, "site", "generated", "learning-center", "market-news");
  const newsRootPrefix = `${newsRoot}${path.sep}`;

  for (const article of articles) {
    const standalonePath = String(article?.standalonePath || "");
    const absolutePath = path.resolve(repoRoot, ...standalonePath.split("/"));
    if (!absolutePath.startsWith(newsRootPrefix)) {
      throw new Error(`Location news target escaped its generated directory: ${standalonePath || "missing"}`);
    }
    if (!fs.existsSync(absolutePath) || !fs.statSync(absolutePath).isFile()) {
      throw new Error(`Location news target is missing: ${standalonePath || article?.route || "unknown article"}`);
    }
  }

  return index;
}

export function generatedRelativePathForRoute(route) {
  if (route === "/") throw new Error("The root document is not generated by this workstream");
  if (
    typeof route !== "string" ||
    !route.startsWith("/") ||
    route.includes("\\") ||
    route.includes("?") ||
    route.includes("#") ||
    route.includes("//") ||
    route.endsWith("/") ||
    route.split("/").some((segment) => segment === "." || segment === "..") ||
    !/^\/[a-z0-9]+(?:[/-][a-z0-9]+)*$/.test(route)
  ) {
    throw new Error(`Cannot generate a noncanonical public route: ${route}`);
  }
  if (route.startsWith("/learning-center/market-news/")) {
    throw new Error(`News route ownership is separate from static public routes: ${route}`);
  }
  return `${route.slice(1)}/index.html`;
}

export function buildStaticRouteFiles(inputs, { siteOrigin = DEFAULT_SITE_ORIGIN } = {}) {
  const context = createStaticRouteContext(inputs);
  const files = new Map();
  for (const entry of context.manifest) {
    if (entry.route === "/") continue;
    const relativePath = generatedRelativePathForRoute(entry.route);
    const record = context.recordsByRoute.get(entry.route);
    files.set(relativePath, renderStaticRouteDocument(record, context, { siteOrigin }));
  }
  for (const redirect of context.tagRedirects || []) {
    const relativePath = generatedRelativePathForRoute(redirect.route);
    files.set(relativePath, renderStaticRouteRedirectDocument(redirect, { siteOrigin }));
  }
  return files;
}

export function compareGeneratedRouteFiles(desired, actual) {
  const missing = [];
  const stale = [];
  const unexpected = [];
  for (const [relativePath, content] of desired) {
    if (!actual.has(relativePath)) {
      missing.push(relativePath);
      continue;
    }
    const desiredBytes = Buffer.from(content, "utf8");
    const actualValue = actual.get(relativePath);
    const actualBytes = Buffer.isBuffer(actualValue) ? actualValue : Buffer.from(actualValue, "utf8");
    if (!desiredBytes.equals(actualBytes)) stale.push(relativePath);
  }
  for (const relativePath of actual.keys()) {
    if (!desired.has(relativePath)) unexpected.push(relativePath);
  }
  return {
    missing: missing.sort(),
    stale: stale.sort(),
    unexpected: unexpected.sort(),
  };
}

function absoluteGeneratedPath(relativePath) {
  const segments = relativePath.split("/");
  const absolutePath = path.resolve(GENERATED_ROUTES_DIR, ...segments);
  const rootPrefix = `${path.resolve(GENERATED_ROUTES_DIR)}${path.sep}`;
  if (!absolutePath.startsWith(rootPrefix)) {
    throw new Error(`Generated route path escaped its output directory: ${relativePath}`);
  }
  return absolutePath;
}

function readGeneratedFiles(directory = GENERATED_ROUTES_DIR, current = directory, files = new Map()) {
  if (!fs.existsSync(current)) return files;
  for (const entry of fs.readdirSync(current, { withFileTypes: true })) {
    const absolutePath = path.join(current, entry.name);
    if (entry.isDirectory()) {
      readGeneratedFiles(directory, absolutePath, files);
      continue;
    }
    if (!entry.isFile()) continue;
    const relativePath = path.relative(directory, absolutePath).split(path.sep).join("/");
    files.set(relativePath, fs.readFileSync(absolutePath));
  }
  return files;
}

function removeEmptyGeneratedDirectories(current = GENERATED_ROUTES_DIR) {
  if (!fs.existsSync(current)) return;
  for (const entry of fs.readdirSync(current, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue;
    removeEmptyGeneratedDirectories(path.join(current, entry.name));
  }
  if (path.resolve(current) === path.resolve(GENERATED_ROUTES_DIR)) return;
  if (fs.readdirSync(current).length === 0) fs.rmdirSync(current);
}

function writeGeneratedFiles(desired, differences) {
  fs.mkdirSync(GENERATED_ROUTES_DIR, { recursive: true });
  for (const relativePath of differences.unexpected) {
    fs.rmSync(absoluteGeneratedPath(relativePath), { force: true });
  }
  for (const relativePath of [...differences.missing, ...differences.stale]) {
    const absolutePath = absoluteGeneratedPath(relativePath);
    fs.mkdirSync(path.dirname(absolutePath), { recursive: true });
    fs.writeFileSync(absolutePath, desired.get(relativePath), "utf8");
  }
  removeEmptyGeneratedDirectories();
}

function loadInputs() {
  return {
    seed: readJson("mock-data/production-seed.json"),
    editorialBundle: readJson("mock-data/editorial-content.json"),
    productCopyBundle: readJson("mock-data/product-copy.json"),
    locationNewsIndex: loadLocationNewsIndex(),
    mediaManifest: readJson("mock-data/location-news-media-manifest.json"),
    ratesMarketplaceFixture: readJson("mock-data/rates-marketplace-fixtures.json"),
    tagRegistry: readJson("mock-data/tag-registry.json"),
    searchIndex: readJson("mock-data/search-index.json"),
  };
}

function reportDifferences(differences) {
  for (const [label, files] of Object.entries(differences)) {
    for (const file of files) console.error(`- ${label}: site/generated/routes/${file}`);
  }
}

export function runStaticRouteGenerator({ check = false } = {}) {
  const desired = buildStaticRouteFiles(loadInputs());
  const actual = readGeneratedFiles();
  const differences = compareGeneratedRouteFiles(desired, actual);
  const differenceCount = Object.values(differences).reduce((sum, files) => sum + files.length, 0);

  if (check) {
    if (differenceCount > 0) {
      console.error("Static route documents are not fresh:");
      reportDifferences(differences);
      return 1;
    }
    console.log(`Static route documents are fresh: ${desired.size} files.`);
    return 0;
  }

  writeGeneratedFiles(desired, differences);
  console.log(`Generated ${desired.size} static route documents (${differences.missing.length + differences.stale.length} written, ${differences.unexpected.length} removed).`);
  return 0;
}

const invokedPath = process.argv[1] ? path.resolve(process.argv[1]) : "";
const isMain = invokedPath && invokedPath.toLowerCase() === fileURLToPath(import.meta.url).toLowerCase();
if (isMain) {
  try {
    process.exitCode = runStaticRouteGenerator({ check: process.argv.includes("--check") });
  } catch (error) {
    console.error(error.stack || error.message);
    process.exitCode = 1;
  }
}
